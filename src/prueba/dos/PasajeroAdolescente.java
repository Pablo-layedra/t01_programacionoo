package prueba.tres;

public class PasajeroAdolescente extends Pasajeros{
	
	private String nombre;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	private String apellido;
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}


	private String deporteDePractica;
	public String getDeporteDePractica() {
		return deporteDePractica;
	}
	public void setDeporteDePractica(String deporteDePractica) {
		this.deporteDePractica = deporteDePractica;
	}
	

}
